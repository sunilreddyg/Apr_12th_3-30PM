package corejava.variables;

public class Datatypes 
{

	public static void main(String[] args) 
	{
		
		
		/*
		 * int:--> Numberic datatype only accept numbers to store
		 * 			Default value is zero  0
		 */
		int a=100;
		int b=200;
		int c=a+b;
		System.out.println("total value => "+c);
		
		
		/*
		 * boolean:-->
		 * 			boolean accept true/false.
		 * 			Default value is false..
		 */
		boolean flag=true;
		System.out.println("flag status is => "+flag);
		
		
		/*
		 * String:-->
		 * 			=>String is a non primitive datatype
		 * 			=>String accept set of characters to store
		 * 			=>And default value is null.
		 */
		
		String name="Mindq";
		String email="info.mindqsystems.com";
		String mobile="9030248855";
		
		System.out.println(name+"    "+email+"    "+mobile);

	}

}
