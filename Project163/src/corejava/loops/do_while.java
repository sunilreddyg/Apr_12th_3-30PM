package corejava.loops;

public class do_while {

	public static void main(String[] args)
	{
		
		/*
		 * Dowhile:-->
		 * 		Dowhile similar to while loop only differance it accept
		 * 		loop once even expected condition not return true..
		 */
		
		
		int m=1;
		do {
			System.out.println(m);
			m=m+1;
			
		} while (m < 20);

	}

}
